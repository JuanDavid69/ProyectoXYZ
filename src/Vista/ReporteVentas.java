    private void graficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_graficarActionPerformed
        try{
            int filas = reporte.getRowCount();
            String titulo = reporte.getColumnName(1) + " POR " + reporte.getColumnName(0); //titulo de la tabla

        
            if(barras.isSelected()){
                DefaultCategoryDataset data = new DefaultCategoryDataset();
                for (int i=0; i < filas; i++){
                    data.setValue(Integer.parseInt(reporte.getValueAt(i, 1).toString()),reporte.getValueAt(i, 0).toString(),reporte.getValueAt(i, 0).toString()); //captura de datos de la tabla para nombre de valores variables.
                }
                JFreeChart grafica = ChartFactory.createBarChart3D(titulo,reporte.getColumnName(0), reporte.getColumnName(1), data, PlotOrientation.VERTICAL, true, true, true); //nombre de valores variables
                ChartFrame panelGrafica = new ChartFrame(titulo, grafica);
                panelGrafica.setSize(600,600);
                panelGrafica.setLocationRelativeTo(null);
                panelGrafica.setVisible(true);

                String [] Titulo = {};
                DefaultTableModel newTabla = new DefaultTableModel(null,Titulo);
                this.reporte.setModel(newTabla);
            }
            if(pastel.isSelected()){
                DefaultPieDataset data = new DefaultKeyedValuesDataset();
                for (int i=0; i < filas; i++){
                    data.setValue(reporte.getValueAt(i, 0).toString(), Integer.parseInt(reporte.getValueAt(i, 1).toString()));//captura de datos de la tabla para nombre de valores manual.
                }
                JFreeChart grafica = ChartFactory.createPieChart(titulo, data, true, true,true); //nombres de valores manuales.
                ChartFrame panelGrafica = new ChartFrame(titulo, grafica);
                panelGrafica.setSize(600,600);
                panelGrafica.setLocationRelativeTo(null);
                panelGrafica.setVisible(true);

                String [] Titulo = {};
                DefaultTableModel newTabla = new DefaultTableModel(null,Titulo);
                this.reporte.setModel(newTabla);
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error al capturar los datos");
            System.out.println(e);
        }
    }//GEN-LAST:event_graficarActionPerformed

