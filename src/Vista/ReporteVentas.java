    private void jButtonGenerarTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerarTablaActionPerformed
        try{
            DefaultTableModel tabla;
            String start = this.start.getDate().toString();
            String end = this.end.getDate().toString();
            if(sedes.isSelected()){
                tabla = controlVenta.ventasSede(start,end);
                reporte.setModel(tabla);
            }
            if(vendedores.isSelected()){
                tabla = controlVenta.ventasVendedor(start,end);
                reporte.setModel(tabla);
            }
            if(ganacias.isSelected()){
                tabla = controlVenta.gananciasSedes(start,end);
                reporte.setModel(tabla);
            }
            if(productos.isSelected()){
                tabla = controlVenta.productosVendidos(start,end);
                reporte.setModel(tabla);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error al capturar los datos");
            System.out.println(e);
        }
    }//GEN-LAST:event_jButtonGenerarTablaActionPerformed

